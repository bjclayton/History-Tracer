package Models;

/**
 * The type Icons (model).
 */
public class Icons {
    private String iconApp, iconCopy, iconSort, iconRefresh, iconSelectAll, loadGif, iconMore;

    /**
     * Instantiates a new Icons.
     *
     * @param iconApp       the icon app
     * @param iconCopy      the icon copy
     * @param iconSort      the icon sort
     * @param iconRefresh   the icon refresh
     * @param iconSelectAll the icon select all
     * @param loadGif       the load gif
     * @param iconMore      the icon more
     */
    public Icons(String iconApp, String iconCopy, String iconSort, String iconRefresh, String iconSelectAll, String loadGif, String iconMore) {
        this.iconApp = iconApp;
        this.iconCopy = iconCopy;
        this.iconSort = iconSort;
        this.iconRefresh = iconRefresh;
        this.iconSelectAll = iconSelectAll;
        this.loadGif = loadGif;
        this.iconMore = iconMore;
    }


    /**
     * Gets icon app.
     *
     * @return the icon app
     */
    public String getIconApp() {
        return iconApp;
    }

    /**
     * Sets icon app.
     *
     * @param iconApp the icon app
     */
    public void setIconApp(String iconApp) {
        this.iconApp = iconApp;
    }

    /**
     * Gets icon copy.
     *
     * @return the icon copy
     */
    public String getIconCopy() {
        return iconCopy;
    }

    /**
     * Sets icon copy.
     *
     * @param iconCopy the icon copy
     */
    public void setIconCopy(String iconCopy) {
        this.iconCopy = iconCopy;
    }

    /**
     * Gets icon sort.
     *
     * @return the icon sort
     */
    public String getIconSort() {
        return iconSort;
    }

    /**
     * Sets icon sort.
     *
     * @param iconSort the icon sort
     */
    public void setIconSort(String iconSort) {
        this.iconSort = iconSort;
    }

    /**
     * Gets icon refresh.
     *
     * @return the icon refresh
     */
    public String getIconRefresh() {
        return iconRefresh;
    }

    /**
     * Sets icon refresh.
     *
     * @param iconRefresh the icon refresh
     */
    public void setIconRefresh(String iconRefresh) {
        this.iconRefresh = iconRefresh;
    }

    /**
     * Gets icon select all.
     *
     * @return the icon select all
     */
    public String getIconSelectAll() {
        return iconSelectAll;
    }

    /**
     * Sets icon select all.
     *
     * @param iconSelectAll the icon select all
     */
    public void setIconSelectAll(String iconSelectAll) {
        this.iconSelectAll = iconSelectAll;
    }

    /**
     * Gets load gif.
     *
     * @return the load gif
     */
    public String getLoadGif() {
        return loadGif;
    }

    /**
     * Sets load gif.
     *
     * @param loadGif the load gif
     */
    public void setLoadGif(String loadGif) {
        this.loadGif = loadGif;
    }


    /**
     * Gets icon more.
     *
     * @return the icon more
     */
    public String getIconMore() {
        return iconMore;
    }


    /**
     * Sets icon more.
     *
     * @param iconMore the icon more
     */
    public void setIconMore(String iconMore) {
        this.iconMore = iconMore;
    }
}
